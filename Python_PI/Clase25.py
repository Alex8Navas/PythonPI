# Clase de Teoría. 

# Clase: Modelo donde se redactan las características comunes de un grupo de objetos. 

# Instancia: Es un objeto o ejemplar perteneciente a una clase. 

# Modularización: Una aplicación compleja tiene varias clases que definen distintos modeulos. 
# Esto permite reutilizar trozos de código en distintos módulos que son independientes. 

# Encapsulación: El funcionamiento interno de los módulos está encapsulado, protegido del exterior.
# Unas clases no regulan el funcionamiento interno de otras a pesar de funcionar como un todo, 
# de estar conectadas. 

# Métodos de acceso: Los enlaces entre las clases que permiten su actuación como un todo. 

# Nomenclatura del Punto: código/sintaxis en POO para definir clases. 

